#define CRT_VIDEO_MEMORY ((volatile PChar)0xB8000)
#define SCREEN_WIDTH 80
#define SCREEN_HEIGHT 25

uchar logBuffer[256 * 256];
u32 logBufferIx = 0;

PVideoGraphicsArray videoGraphicsArray;

VgaConsole *vgaConsole;

struct VgaConsole vgaBootConsole = {
    0, 0, CLR_WHITE, CLR_BLACK, 80, 25, 0, 0, 8, 8, (PChar)FONT, false
};

